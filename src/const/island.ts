// 国別のコード一覧（ISO 3166-1 alpha-2）
export const COUNTRY_CODES = {
  JAPAN: 'JP',
  // 他の国のコードもここに追加可能
};

// 住所種類の定義
export enum AddressType {
  DOMESTIC_MAINLAND = 'domestic_mainland', // 国内陸地
  DOMESTIC_ISLAND = 'domestic_island',     // 国内離島
  INTERNATIONAL = 'international'          // 海外
}

/**
 * 住所から住所種類（国内陸地・国内離島・海外）を判定する
 * @param prefecture 都道府県名
 * @param address 市区町村以下の住所
 * @param country 国コード（デフォルトは日本）
 * @returns 住所種類（AddressType）
 */
export function getAddressType(
  prefecture: string,
  address: string,
  country: string = COUNTRY_CODES.JAPAN
): AddressType {
  // 日本以外の国は全て国際的とみなす
  if (country !== COUNTRY_CODES.JAPAN) {
    return AddressType.INTERNATIONAL;
  }

  // 離島リストと照合
  const isIsland = JAPANESE_ISLANDS.some(island => {
    // 都道府県が一致し、住所が島のアドレスで始まる場合
    return (
      island.prefecture === prefecture &&
      (
        // 住所が完全一致、または
        island.address === address ||
        // 住所が離島アドレスで始まる（より詳細な住所の場合）
        address.includes(island.address)
      )
    );
  });

  return isIsland ? AddressType.DOMESTIC_ISLAND : AddressType.DOMESTIC_MAINLAND;
}

